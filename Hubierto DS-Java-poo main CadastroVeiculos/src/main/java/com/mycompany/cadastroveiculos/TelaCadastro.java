    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
    String marca = JOptionPane.showInputDialog(this, "Digite a marca do veÃ­culo que deseja remover:");

    if (marca != null && !marca.isEmpty()) {
        boolean removido = false;

        for (int i = 0; i < listaVeiculos.size(); i++) {
            if (listaVeiculos.get(i).getMarca().equalsIgnoreCase(marca)) {
                listaVeiculos.remove(i);
                removido = true;
                break;
            }
        }

        if (removido) {
            JOptionPane.showMessageDialog(this, "VeÃ­culo removido com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "VeÃ­culo nÃ£o encontrado.");
        }
    }

    }//GEN-LAST:event_btnRemoverActionPerformed

